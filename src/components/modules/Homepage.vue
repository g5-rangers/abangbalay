    <v-card id="card" flat class="mx-auto" max-width="650px">
      <center>
        <div>
          <img :src="require('@/assets/images/MyFrontLogo.png')" />
        </div>
        <v-card-text>
          <div>
            <h4>New in Cebu? Just stay calm, wait and search.</h4>
            <h4>We will find safe home for you..</h4>
          </div>
        </v-card-text>
        <v-col cols="12" sm="6">
          <v-toolbar dark color="orange darken-2" >
            <v-autocomplete
              v-model="select"
              :loading="loading"
              :items="items.map(item=>item.address)"
              :search-input.sync="search"
              cache-items
              class="mx-2"
              flat
              color="black"
              hide-no-data
              hide-details
              label="e.g Talamban"
              prepend-inner-icon="mdi-magnify"
              solo-inverted
            ></v-autocomplete>
          </v-toolbar>
        </v-col>
      </center>
    </v-card>
    <Results v-for="(details ,i) in items " :key="i" :Details="details"></Results>
