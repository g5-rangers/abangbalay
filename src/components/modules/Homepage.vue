<script>
import Results from "components/modules/Results.vue";
import axios from "axios";

export default {
  data() {
    return {
      loading: false,
      items: [1,1],
      search: null,
      select: null
    };
  },
  watch: {
    search(val) {
      val && val !== this.select && this.querySelections(val);
    }
  },
  components: {
    Results
  },
  methods: {
    querySelections(val) {
      console.log(val);
      this.loading = true;
      // Simulated ajax query
      axios
        .get(`http://localhost:4000/user/search/${val}`)
        .then(res => {
          this.loading = false;
          this.items = res.data;
        })
        .catch(err => {
          this.false;
          console.log(err.response);
        });
    }
  }
};
</script>